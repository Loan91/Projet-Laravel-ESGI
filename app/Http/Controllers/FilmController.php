    {
        $validatedData = $request->validate([
            'date_sortie' => 'required|date',
        ]);

        $film = new Film();

        $film->titre = $request->titre;
        $film->genre = $request->genre;
        $film->date_sortie = Carbon::create($request->date_sortie);

        $film->save();

        return redirect('films');
    }

